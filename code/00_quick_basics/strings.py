# 0.2 字符串与编码 — 代码示例 (Python 3.12+)

# ============================================================
# 创建字符串：单引号、双引号、三引号
# ============================================================

s1 = 'hello'
s2 = "world"
s3 = """多行
字符串"""
s4 = '''也可以
用单引号'''

print(f"长度: {len(s1)}, {len(s3)}")

# ============================================================
# f-string — 几乎可以在 {} 里写任何 Python 表达式
# ============================================================

name, age = "张三", 25
print(f"{name} 今年 {age} 岁，明年 {age + 1} 岁")
print(f"name 是大写吗？{name.isupper() = }")  # 3.8+ 调试语法

# 格式化数字
pi = 3.1415926
print(f"π ≈ {pi:.2f}")          # 保留两位小数
print(f"0.25 = {0.25:.1%}")     # 百分比

# ============================================================
# 常用字符串方法
# ============================================================

s = "  Hello World!  "
print(f"strip():  '{s.strip()}'")       # 去首尾空白
print(f"lower():  '{s.lower()}'")       # 全小写
print(f"replace(): '{s.replace('World', 'Python')}'")
print(f"是否以 'He' 开头: {s.strip().startswith('He')}")

# split — 字符串 → 列表
fruits = "apple,banana,grape"
print(f"split: {fruits.split(',')}")

# join — 列表 → 字符串
words = ["Python", "is", "fun"]
print(f"join: {' '.join(words)}")

# ============================================================
# 编码：str（文本）→ bytes（二进制）→ str（文本）
# ============================================================

text = "Python 你好"
# str → bytes：编码
encoded = text.encode("utf-8")
print(f"UTF-8 编码后: {encoded}")
print(f"共 {len(encoded)} 个字节")

# bytes → str：解码
decoded = encoded.decode("utf-8")
print(f"解码后: {decoded}")
print(f"来回一致: {text == decoded}")

# ============================================================
# 3.12 新特性：引号嵌套更自由
# ============================================================

# 3.12 起，f-string 中可以更自由地使用引号
d = {"name": "Python", "version": 3.12}
# Python 3.12+: print(f"语言: {d["name"]}")   # 嵌套双引号
print(f"语言: {d['name']}")   # 3.11 兼容写法
