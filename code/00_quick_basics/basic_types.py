# 0.1 变量与基本类型 — 代码示例 (Python 3.12+)

# ============================================================
# 动态类型：变量只是"名字标签"，可以随时指向不同类型的对象
# ============================================================

x: int = 42                    # 类型注解只是提示，不强制
print(f"x = {x}, type = {type(x)}")   # x = 42, type = <class 'int'>

x = "hello"                    # 同一个名字，现在指向字符串
print(f"x = {x}, type = {type(x)}")   # x = hello, type = <class 'str'>

# ============================================================
# 基本类型：int, float, bool, None
# ============================================================

# int — 任意精度，不会溢出
big = 2 ** 100                 # 1267650600228229401496703205376
print(f"2**100 = {big}")

# float — 双精度 IEEE 754
pi: float = 3.14
print(f"pi = {pi}, type = {type(pi)}")

# bool — 是 int 的子类
print(f"True == 1: {True == 1}")       # True
print(f"False == 0: {False == 0}")     # True

# None — 空值，单例对象
result = None
print(f"result is None: {result is None}")  # True

# ============================================================
# type() vs isinstance() — 类型检查
# ============================================================

print(f"isinstance(True, int): {isinstance(True, int)}")  # True!
# isinstance 会检查继承链，所以 True 既是 bool 也是 int

# ============================================================
# 多变量赋值（解包）
# ============================================================

a, b, c = 1, 2, 3             # 同时给多个名字赋值
print(f"a={a}, b={b}, c={c}")

# 交换一行搞定（Python 特色）
a, b = b, a
print(f"交换后: a={a}, b={b}")
